using System;

namespace ConsoleApp1
{
    using System;

    class Program
    {
        static void Main()
        {
            // Массив с очками команд в порядке убывания
            int[] points = { 30, 28, 25, 22, 20, 18, 15, 10, 8, 5 };
            int N; // Переменная для очков команды

            while (true) // Бесконечный цикл для повторного ввода
            {
                Console.Title = "Практическая работа №5"; // заголовок консоли
                Console.BackgroundColor = ConsoleColor.DarkGreen;// цвет консоли
                Console.ForegroundColor = ConsoleColor.DarkBlue;// цвет текста консоли
                
                Console.Write("Введите количество очков команды (N): ");
                N = int.Parse(Console.ReadLine()); // Вводим количество очков

                int place = 1; // Переменная для хранения места команды

                for (int i = 0; i < points.Length; i++) // Проход по массиву очков
                {
                    if (points[i] == N) // Если очки совпадают
                    {
                        Console.WriteLine($"Команда с {N} очками заняла {place} место.");
                        break; // Выход из цикла, если команда найдена
                    }
                    place++; // Увеличиваем место, если команда не найдена
                }

                Console.WriteLine("Хотите ввести другие очки? (да/нет)");
                string answer = Console.ReadLine().ToLower();
                if (answer != "да") // Проверяем, хочет ли пользователь продолжить
                    break; // Если нет, выходим из программы
            }
        }
    }
}